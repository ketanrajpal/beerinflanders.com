<section id="about" class="light">
  <h1>提升质量促进“出口”的早期措施</h1>
  <p>在16、17世纪，保证啤酒质量的法规数量逐渐增多。在德国，1516年颁布的《纯净法》（"Reinheitsgebot"）规定啤酒只能由大麦、啤酒花和水酿造，而在哈雷和佛兰德布拉班特，从1559年开始使用一种特定的麦芽浆来酿造拉比克啤酒。<br>
    <br>
    从17世纪开始，各个地区开始创造出不同的啤酒，比如安特卫普的大麦啤酒（gerstenbier），鲁汶的白啤酒（Leuvense witte），迪斯特和奥德纳尔德的棕啤，以及里尔的酒窖啤酒（caves）。酿酒师们也逐渐开始向其它区域“出口”他们的啤酒。<br>
    <br>
    修道院享有的特权在18世纪末宣告终结。1783年，皇帝约瑟夫二世解散修道院，因为它们侵犯了啤酒厂的利益，另有一些修道院和它们的酿酒厂在法国大革命中被摧毁。<br>
    <br>
    19世纪开启了啤酒历史的崭新篇章，因为捷克皮尔森啤酒取得重大突破（1839），它在酒体混浊的黑啤主导的市场中迅速取得成功。在工业革命期间，科学家们又对酿制过程和酵母培养有了更深刻的认识。</p>
</section>
