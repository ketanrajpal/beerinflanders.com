  <h1>混合草药和啤酒花</h1>
  <p>在中世纪时期，人们第一次用一种叫做“Gruit”的混合草药来为啤酒调味。酿酒者必须去叫做“Gruithuis”的专门商店购买这种草药（布鲁日的Gruuthuuse），但是修道院则不必这么麻烦，而是开始使用啤酒花，因为它可以帮助保存啤酒，延长啤酒保质期。在十一世纪，阿夫利赫姆的本笃会修道院在向法兰德斯地区引进啤酒花种植方面发挥了重要作用。<br>
    <br>
