   1833年、デ・コーニンク醸造所がヨハネス・ブェルブリートより起業されました。アントワープの一番古い醸造所として、この港町のシンボルでもあります。アントワープとの連携を強化するため、2015年の春にデ・コーニンク醸造所が創意に溢れ、世間に知られるビール体験センターを設立しました。観光客は生のビール文化を体験するだけでなく、アントワープ市のビール醸造の歴史を知ることもできます。体験センターには9つのテーマ館があり、最新の視聴技術でアントワープの歴史を紹介することで、ビールの町としての独自性とデ・コーニンクビールの醸造プロセスを展示します。
